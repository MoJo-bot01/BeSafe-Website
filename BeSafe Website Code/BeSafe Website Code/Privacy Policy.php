BE SAFE Privacy Policy
 

Introduction
BE SAFE is committed to protecting the privacy and confidentiality of individuals who visit our website or seek support related to gender-based violence (GBV). This Privacy Policy outlines our practices regarding the collection, use, and disclosure of personal information.

Information We Collect
Personal Information
We may collect the following personal information when you visit our website or interact with our services:

Name
Contact Information (e.g., email address, phone number)
Demographic Information (e.g., age, gender)
Any other information you choose to provide voluntarily
Non-Personal Information
We may also collect non-personal information, such as browser type, operating system, and IP address, to improve the functionality of our website and services.

How We Use Your Information
We use the collected information for the following purposes:

Providing support and resources related to GBV
Responding to inquiries and requests for assistance
Improving our website and services
Conducting research and analysis to better understand GBV issues
Communicating with you about our programs, events, and updates
Information Sharing
We do not share personal information with third parties without your consent, except in the following circumstances:

When required by law or legal process
To protect the safety and well-being of individuals
To prevent fraud or illegal activities
Security Measures
We employ reasonable technical and organizational measures to safeguard the information we collect from unauthorized access, disclosure, alteration, or destruction.

Consent
By using our website and services, you consent to the terms outlined in this Privacy Policy.

Links to External Websites
Our website may contain links to external sites. We are not responsible for the privacy practices or content of these sites.

Children's Privacy
Our services are not intended for individuals under the age of 18. If you believe a child has provided us with personal information, please contact us immediately.

Changes to this Policy
We may update this Privacy Policy from time to time. Any changes will be reflected on this page.

